sub unique_commands_verbose
{
	my %seen;
	my @array = (@_);
	my @unique = ();
	foreach my $value (@array) 
	{
		my @entrys = split (";", $value);
		my $entry = $entrys[1];
		if ( $entry =~ /^(.+?)\s.+$/ ) 
		{
			my $command = $1;
			if (!defined($seen{$command})) { 
				push @unique, $value;
				$seen{$command} = 1;
			}
			else{
				PrintLog("Duplicate Command: $command\n");
			}
		}
		else{
			PrintLog("Command $entry syntax ERROR did not match command.\n");
		}
	}
	return @unique;
}


