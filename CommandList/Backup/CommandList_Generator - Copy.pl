sub unique_hash
{
	my %seen;
	my @array = (@_);
	my @unique = ();
	my $command;
	
	#PrintLog(Dumper(@array));
	
	foreach my $k (@array) 
	{
		if ( $k->{name} )
		{
			if (!defined($seen{$k->{name}})) { 
				push @unique, $k;
				$seen{$command} = 1;
			}
		}
	}
	return @unique;
}

sub unique_hash2
{
	my $ref = $_[0];
	my %seen;
	my $removed = 0;
	for my $i (0 .. @$ref-1) {
		my $item = $ref->[$i];
		$seen{$item->{name}}++ ? $removed++ : ($ref->[$i-$removed] = $item);
		if (defined($seen{$item->{name}})) { 
			PrintLog("Duplicate entry found " . $item->{name} . " Plugin: " . $item->{plugin} . "\n");
		}
		
	}
	splice @$ref,-$removed;
}


